//マウスオーバー時の処理
function mouseOn(){
	let about = document.getElementById("about");
	about.src = "images/letter-about.png";
}

//マウスアウト時の処理

function mouseOff(){
	let about = document.getElementById("about");
	about.src = "images/letter-about-cl.png";
}


